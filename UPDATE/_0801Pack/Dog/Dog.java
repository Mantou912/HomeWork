package HomeWork.UPDATE._0801Pack.Dog;

public class Dog {
    String Name;
    String Strain;

    public Dog() {
    }

    public Dog(String name, String strain) {
        Name = name;
        Strain = strain;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getStrain() {
        return Strain;
    }

    public void setStrain(String strain) {
        Strain = strain;
    }
}
